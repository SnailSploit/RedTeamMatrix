// Generates docs/EMERGING-GAPS.md — the CROSS OF OLD AND NEW.
// Reads the canonical model, runs the composition predictor (compose.ts), and writes the
// emerging-gap analysis: where a frontier/undetected NEW entry seam fuses onto a mature/
// well-tooled OLD propagation seam at a shared principal. The emergent gap is the BRIDGE
// itself — the new entry lives in MITRE ATLAS (or is unmodeled), the old propagation lives
// in Enterprise ATT&CK, and no single taxonomy spans the cross. Run: node gen-emerging-gaps.mjs
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { loadDataset } from "./src/model.ts";
import { predictComposites, predictChains3 } from "./src/compose.ts";

const HERE = dirname(fileURLToPath(import.meta.url));
const ds = loadDataset();
const byId = new Map(ds.seams.map((s) => [s.id, s]));
const pN = new Map(ds.principals.map((p) => [p.id, p.name]));
const nm = (id) => pN.get(id) ?? id;
const techOf = (s) => s.techniques[0]?.name ?? s.id;
const attackIds = (s) => (s.techniques[0]?.attack_ids ?? []).filter(Boolean);
// unmodeled === NO Enterprise/mapped ATT&CK id on ANY of the seam's techniques (matches emergingSummary).
const unmodeled = (s) => s.techniques.every((t) => !t.attack_ids || t.attack_ids.length === 0);
const gapOf = (s) => s.tech_note?.mitre_gap ?? null;

const comp = predictComposites(ds, 100000); // full set, matches meta.emerging
const chains = predictChains3(ds, 25);

// --- aggregate the structure of the cross -------------------------------------------------
const junc = new Map();                                       // shared node -> # of crosses
for (const c of comp) junc.set(c.node, (junc.get(c.node) ?? 0) + 1);
const juncTop = [...junc.entries()].sort((a, b) => b[1] - a[1]);

const leverage = new Map();                                   // OLD propagation seam -> {n, peak}
for (const c of comp) {
  const v = leverage.get(c.propagate_seam) ?? { n: 0, peak: 0 };
  v.n++; v.peak = Math.max(v.peak, c.score); leverage.set(c.propagate_seam, v);
}
const leverTop = [...leverage.entries()].sort((a, b) => b[1].n - a[1].n);

const pattern = new Map();                                    // "newP→oldP" -> count
for (const c of comp) {
  const k = `${c.primitives[0]}→${c.primitives[1]}`;
  pattern.set(k, (pattern.get(k) ?? 0) + 1);
}
const patTop = [...pattern.entries()].sort((a, b) => b[1] - a[1]);

// share of crosses whose NEW entry is unmodeled by Enterprise ATT&CK (ATLAS-only or none)
const bridgeCount = comp.filter((c) => unmodeled(byId.get(c.entry_seam))).length;
const bridgePct = Math.round((100 * bridgeCount) / comp.length);

const PRIMNAME = {
  P1: "Data→Control", P2: "Identity→Authority", P3: "Provenance",
  P4: "Context-Inheritance", P5: "Format-Boundary", P6: "Time/State",
};

// --- write -------------------------------------------------------------------------------
let md = `# Emerging Gaps — the Cross of Old and New

The dangerous 2025–2027 attacks are not new techniques. They are a **new, unmonitored entry**
welded onto a **proven, well-tooled classic** at a shared principal. SEAMMAP makes that cross
*predictable*: \`src/compose.ts\` enumerates every \`(frontier/undetected entry B) × (mature/reliable
propagation A)\` that meet at a node \`n\` — \`B.target ∋ n\` and \`A.source ∋ n\`. The composite inherits
**B's zero detection** and **A's reliability**.

> Generated by \`gen-emerging-gaps.mjs\` from the canonical model + \`src/compose.ts\`. ${comp.length} two-edge
> composites and ${chains.length} three-hop chains computed; nothing hand-authored. Authorized red-team / research use only.

## Why the cross is the gap (and why no taxonomy names it)

The emergent vulnerability is the **bridge**, not either end. And the bridge falls *between* the
catalogues: of the ${comp.length} composites, **${bridgeCount} (${bridgePct}%) have a NEW entry that
Enterprise ATT&CK does not model at all** — it lives in MITRE ATLAS (the adversarial-ML matrix Enterprise
does not incorporate) or in no framework — while the OLD propagation is a first-class Enterprise technique.

- A defender watching **ATLAS** sees the prompt-injection / agent-identity entry, but ATLAS has no language
  for the Kerberoast / DCSync / IAM-chaining that follows.
- A defender watching **Enterprise** sees ordinary mature activity (it *is* ordinary — that's the point),
  but never connects it to the unmonitored agentic door it came through.
- No one owns the seam in between. That is the emerging gap.

## Where old meets new — the junction principals

Old and new fuse at a handful of nodes. Instrumenting these junctions is the highest-leverage detection move.

| Junction principal | Old×new crosses through it |
|---|---:|
`;
for (const [n, c] of juncTop.slice(0, 8)) md += `| ${nm(n)} | ${c} |\n`;

md += `\n## The dominant merge pattern: everything-new → **P2 Identity→Authority**

The new entry's primitive rarely matters; the *old* side it lands on almost always does — and it is
overwhelmingly **P2 (Identity→Authority)**. The agentic surface (P1 data→control injection, P3 forged
provenance, P4 inherited context, P6 time/state) is a way *in*; classic identity and cloud-IAM escalation
is how it *travels*.

| New primitive → Old primitive | Composites |
|---|---:|
`;
for (const [k, c] of patTop.slice(0, 10)) {
  const [a, b] = k.split("→");
  md += `| ${k}  *(${PRIMNAME[a]} → ${PRIMNAME[b]})* | ${c} |\n`;
}

md += `\n## The most-leveraged OLD techniques

These mature classics are the propagation engines the most new entries feed into — harden/monitor these
and you blunt many emergent composites at once.

| Old technique (propagation) | Prim | New entries that feed it | Peak score |
|---|---|---:|---:|
`;
for (const [id, v] of leverTop.slice(0, 10)) {
  const s = byId.get(id);
  md += `| ${techOf(s)} | ${s.primitive} | ${v.n} | ${v.peak} |\n`;
}

// --- emerging-gap classes (derived from the top composites, grouped by junction) ----------
md += `\n## The emerging-gap classes\n\nThe top composites cluster into a few recurring classes. Each is an
attack a single taxonomy structurally cannot name.\n`;

const TOPN = 16;
const seen = new Set();
let cls = 0;
for (const c of comp) {
  if (cls >= TOPN) break;
  const B = byId.get(c.entry_seam), A = byId.get(c.propagate_seam);
  const key = `${c.entry_seam}|${c.propagate_seam}`;
  if (seen.has(key)) continue;
  seen.add(key); cls++;
  const bGap = gapOf(B);
  const bIds = attackIds(B), aIds = attackIds(A);
  md += `\n### ${cls}. ${techOf(B)} → ${techOf(A)}\n`;
  md += `**Junction:** ${nm(c.node)} · **merge:** ${c.primitives[0]} ${PRIMNAME[c.primitives[0]]} → ${c.primitives[1]} ${PRIMNAME[c.primitives[1]]} · **amplification:** ${c.score}\n\n`;
  md += `- **New door (\`${B.id}\`):** ${B.trust_assumption} *(detection: ${B.detection_status}, tooling: ${B.tooling_status}${bIds.length ? `; ATT&CK: ${bIds.join(", ")}` : "; **no Enterprise ATT&CK id**"})*\n`;
  md += `- **Old engine (\`${A.id}\`):** ${techOf(A)} *(tooling: ${A.tooling_status}${aIds.length ? `; ATT&CK: ${aIds.join(", ")}` : ""})*\n`;
  md += `- **The cross:** ${c.prediction}\n`;
  if (bGap) md += `- **Why no taxonomy names it:** ${bGap}\n`;
  else if (unmodeled(B)) md += `- **Why no taxonomy names it:** the entry has no Enterprise ATT&CK technique, so the bridge from it to a first-class Enterprise propagation crosses a catalogue boundary no matrix spans.\n`;
}

// --- 3-hop chains -------------------------------------------------------------------------
md += `\n## Three-hop chains — one new door, an entire classic kill-chain\n\nThe deepest emergent form: a
single undetected frontier entry, then **two proven classic hops** that each look like ordinary mature
activity in isolation. The defender is least likely to connect these, because the entry surface is
unmonitored and neither propagation hop is anomalous on its own.\n`;
chains.slice(0, 10).forEach((c, i) => {
  const B = byId.get(c.entry_seam);
  md += `\n**${i + 1}.** *(score ${c.score}; entry detection: ${B.detection_status})* ${c.prediction}\n`;
});

md += `\n## What to do with this\n\n1. **Instrument the junction principals** (${juncTop.slice(0, 3).map(([n]) => nm(n)).join(", ")}) — the cross can only happen where both edges already exist.\n`;
md += `2. **Correlate across the ATLAS↔Enterprise boundary.** The single highest-value detection is a rule that ties an agentic/identity anomaly (the new door) to classic IAM/AD escalation (the old engine) — the link no single matrix expresses.\n`;
md += `3. **Treat the most-leveraged old techniques as choke points** — hardening ${leverTop.slice(0, 2).map(([id]) => `"${techOf(byId.get(id))}"`).join(" and ")} blunts many composites at once.\n`;
md += `\n---\n\n*Generated by \`gen-emerging-gaps.mjs\` from \`data/seams.json\` + \`src/compose.ts\`. ${comp.length} composites, ${chains.length} three-hop chains; ${bridgePct}% of composites bridge an ATLAS-only/unmodeled entry to an Enterprise propagation.*\n`;

writeFileSync(join(HERE, "docs", "EMERGING-GAPS.md"), md);
console.log(`wrote docs/EMERGING-GAPS.md — ${comp.length} composites, ${chains.length} chains, ${bridgePct}% cross-taxonomy bridges`);
