// Generates docs/WALKTHROUGHS.md — a detailed walkthrough of every new (AGENT-DISCOVERED)
// technique, assembled from the model (seams + validations + artifacts) so nothing is
// invented: trust assumption, attack mechanics, runnable PoC, success criterion, refuter,
// controls, validity, and MITRE status. Run: node gen-walkthroughs.cjs
const fs = require("fs"), path = require("path");
const D = path.join(__dirname, "data");
const J = (f) => JSON.parse(fs.readFileSync(path.join(D, f), "utf8"));
const seams = J("seams.json").seams;
const prims = J("primitives.json").primitives;
const PRIM = Object.fromEntries(prims.map((p) => [p.id, p.name]));
const principals = Object.fromEntries(J("principals.json").principals.map((p) => [p.id, p.name]));
const vals = J("validations.json");
const arts = J("artifacts.json");
for (const s of seams) { if (vals[s.id]) s.validation = vals[s.id]; if (arts[s.id]) s.test_artifact = arts[s.id]; }
const mitreAbsent = (s) => s.techniques.every((t) => !t.attack_ids || t.attack_ids.length === 0);

const GROUPS = [
  ["seed", "Seed self-audit — frontier sub-seams & node-pair diagonals", (s) => /^(F\d|X-)/.test(s.id)],
  ["GH1", "AI / agent ecosystem", (s) => s.id.startsWith("GH1-")],
  ["GH2", "Enterprise defense-automation", (s) => s.id.startsWith("GH2-")],
  ["GH3", "AI × physical / embedded convergence", (s) => s.id.startsWith("GH3-")],
  ["GH4", "Data-centric / privacy / identity sprawl", (s) => s.id.startsWith("GH4-")],
  ["GH5", "Crypto migration / edge / telecom", (s) => s.id.startsWith("GH5-")],
  ["GH6", "Decentralized / Web3", (s) => s.id.startsWith("GH6-")],
  ["GH7", "PNT / space / cyber-physical", (s) => s.id.startsWith("GH7-")],
  ["GH8", "AI supply-chain / developer-trust", (s) => s.id.startsWith("GH8-")],
  ["GH9", "SaaS-to-SaaS / integration economy", (s) => s.id.startsWith("GH9-")],
  ["GH10", "Data / secrets / MLOps infrastructure", (s) => s.id.startsWith("GH10-")],
];

const disc = seams.filter((s) => s.origin === "AGENT-DISCOVERED");
const nm = (id) => principals[id] || id;
const vrank = { demonstrated: 0, plausible: 1, speculative: 2 };

let md = `# SEAMMAP — Detailed Walkthroughs of the New Techniques\n\n` +
  `A full walkthrough of every AGENT-DISCOVERED technique (${disc.length}), assembled from the canonical model — ` +
  `trust assumption, attack mechanics, a runnable proof-of-concept, the success criterion, how to refute it, ` +
  `existing controls, the adversarial validity verdict, and MITRE status.\n\n` +
  `> Generated by \`gen-walkthroughs.cjs\` from \`data/seams.json\` + \`validations.json\` + \`artifacts.json\`. ` +
  `Authorized red-team / research use only; every PoC is defanged and lab/owned-asset scoped.\n`;

let n = 0;
for (const [, title, match] of GROUPS) {
  const items = disc.filter(match).sort((a, b) => (vrank[a.validation?.validity] ?? 3) - (vrank[b.validation?.validity] ?? 3));
  if (!items.length) continue;
  md += `\n---\n\n# ${title}  *(${items.length})*\n`;
  for (const s of items) {
    n++;
    const t = s.techniques[0] || {};
    const refs = [...(t.attack_ids || []), ...(t.cwe_ids || [])].join(", ");
    const v = s.validation, a = s.test_artifact;
    md += `\n## ${n}. ${t.name || s.id}\n\n`;
    md += `| | |\n|---|---|\n`;
    md += `| **Seam id** | \`${s.id}\` |\n`;
    md += `| **Primitive** | ${s.primitive} ${PRIM[s.primitive]} |\n`;
    md += `| **Trust frame** | ${s.source.map(nm).join(" + ")} → ${s.target.map(nm).join(" + ")} |\n`;
    md += `| **Category** | ${s.classic_branches.join("; ")} |\n`;
    md += `| **Validity** | ${v ? `${v.validity} (${v.confidence} confidence)` : "unrated"} |\n`;
    md += `| **MITRE** | ${mitreAbsent(s) ? "none — unmodeled by ATT&CK" : refs} |\n`;
    md += `| **Maturity / tooling / detection** | ${s.maturity} / ${s.tooling_status} / ${s.detection_status} |\n\n`;
    md += `**The trust assumption.** ${s.trust_assumption}\n\n`;
    md += `**The attack.** ${s.violation}\n\n`;
    if (s.rationale) md += `**Why it's new (and what MITRE misses).** ${s.rationale}\n\n`;
    if (v) {
      md += `**How to validate it.** ${v.validation_method}\n\n`;
      md += `**Refuted if.** ${v.falsifier}\n\n`;
      if (v.prerequisites) md += `**Prerequisites.** ${v.prerequisites}\n\n`;
      if (v.existing_controls) md += `**Existing controls.** ${v.existing_controls}\n\n`;
    }
    if (a) {
      md += `**Proof-of-concept — ${a.name} (\`${a.language}\`).**\n`;
      md += `> ⚠ ${a.authorization}\n\n`;
      if (a.setup) md += `*Setup:* ${a.setup}\n\n`;
      md += "```" + (a.language === "text" ? "" : a.language) + "\n" + a.script.trim() + "\n```\n\n";
      md += `*Success:* ${a.success_criterion}  \n*Cleanup:* ${a.cleanup}  \n*Safety:* ${a.safety}\n\n`;
    }
    if (s.suggested_research) md += `**Research hook.** ${s.suggested_research}\n\n`;
  }
}
fs.writeFileSync(path.join(D, "..", "docs", "WALKTHROUGHS.md"), md);
console.log(`wrote docs/WALKTHROUGHS.md — ${n} walkthroughs, ${md.split("\n").length} lines`);
